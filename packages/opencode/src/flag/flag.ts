import { Config } from "effect"

// Arcanea dual-env support: ARCANEA_* takes priority, OPENCODE_* as fallback
function env(arcanea: string, opencode: string): string | undefined {
  return process.env[arcanea] ?? process.env[opencode]
}

function truthy(key: string) {
  const value = process.env[key]?.toLowerCase()
  return value === "true" || value === "1"
}

function truthyDual(arcanea: string, opencode: string) {
  return truthy(arcanea) || truthy(opencode)
}

function falsy(key: string) {
  const value = process.env[key]?.toLowerCase()
  return value === "false" || value === "0"
}

function falsyDual(arcanea: string, opencode: string) {
  return falsy(arcanea) || falsy(opencode)
}

