// Dynamic getter for OPENCODE_DISABLE_PROJECT_CONFIG
// Supports both ARCANEA_ and OPENCODE_ prefixes
Object.defineProperty(Flag, "OPENCODE_DISABLE_PROJECT_CONFIG", {
  get() {
    return truthyDual("ARCANEA_DISABLE_PROJECT_CONFIG", "OPENCODE_DISABLE_PROJECT_CONFIG")
  },
  enumerable: true,
  configurable: false,
})

